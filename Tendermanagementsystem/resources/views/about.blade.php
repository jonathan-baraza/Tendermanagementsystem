			<p>In Kenya, tender business is open for every existing and legally registered entity or company. Tender business will always be there for every operating business in Kenya or internationally. When you understand the tendering process in Kenya this takes you one step ahead, who knows you can be the next lucy business man/woman supplying the government of Kenya two million laptops for primary school kids. Supply of goods and services is the key thing in the tendering business in Kenya.

It is important to have ready supply of goods and have good financial  backing. You have to source the goods needed in the appropriate time because failure to meet the rules of the contract leads to breach of contract and it is a court case. Getting ready for the business is the key thing because winning a tender contract in Kenya is as easy as long you are potential.  

 
The main areas of supply will include infrastructure, consultancy, transport, construction, information technology, defense, World Bank, health services, engineering cleaning services, event organization and security services.

You can win tender either in the private sector or public sector in Kenya, it is always good to keep on searching where there is tender, this can be done though website search or using other search tools where tender are frequently advised or physical enquiries. There will be a very short notice given in most tenders for they will need a quick supply to continue with their business operation.

Market your business to the procuring officers, let the companies know that you are there, once you market yourself then in case of any business opening the procuring officer will look for you. According to the tendering law in Kenya, tendering procedure needs a prequalification stage, in this stage the procuring officers lists all the potential suppliers of a certain good or service, they will select some firms then invite them to express their expression of interest. The selected firms will then be requested to tender.</p>
		</div>
		<div class="col-5" style="background-color: white; height: 700px; border: 1px solid black;">
