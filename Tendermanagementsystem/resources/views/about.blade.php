	The Tender Management System is targeted at the County government but can scale up to the National Government. The Government works as a whole to acquire tenders and distribute them accordingly to the public.
	The government at large is responsible for managing the tenders of the country. Government tenders are often referred to as public tenders because the government is supposed to display them to the public for application and manage all the functionalities that exist from the tender listing stage to the tender awarding stage. 
	It makes the public aware of the available tenders for application and then decides on who gets the tender and what the parameters for awarding the tender are. 

